                    <p>Brake calipers are often overlooked. But they are an important component of your vehicle's appearance. Most vehicles come with unpainted calipers from the manufacturer and most of the time are cast metal and tend to become rusty. A coat of paint makes the caliper look good and protects it against corrosion and it also makes it easier to keep clean.</p>
                    
                    <p>The brake calipers are not removed from the car to carry out the painting process, as this would require disconnecting and disturbing hydraulic brake lines and would require bleeding of the system, which would add considerable additional time and expense to the process. Removing the calipers would also create a safety issue with the antilock brake system.</p>
                    
                    <p>Painting your brake calipers has functional benefits and it upgrades your vehicle's look and style. The caliper painting process consists of cleaning your calipers off existing grime, rust deposits and uneven surfaces. We will make sure that the calipers are in the best condition possible prior to painting. We will sand them as needed, than apply a coat of primer, than multiple coats of paint, until the desired shade is obtained. And finally, a clear coat is applied for paint protection. For extra added protection and glassy look, multiple coats of Ceramic Pro can be applied.</p>
                                        
                    
